package Swingy.models.Equipment;

public class Equipment {
	protected String	equipmentType;
	protected String	type;
	protected int		bonus;

	public String	getEquipmentType() {
		return this.equipmentType;
	}
	public String	getType() {
		return this.type;
	}
	public int		getBonus() {
		return this.bonus;
	}
}